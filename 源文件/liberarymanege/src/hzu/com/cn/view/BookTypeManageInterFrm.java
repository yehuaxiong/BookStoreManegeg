	private void jBModifyActionPerformed(java.awt.event.ActionEvent evt) {
		String id = this.jBookTypeId.getText();
		String bookTypeName=this.jBookTypeTxt.getText();
		String bookTypeDesc=this.jBookTypeDescTxt.getText();
		if ("".equals(id) || id == null) {
			JOptionPane.showMessageDialog(null, "请选择要修改的记录");
			return;
		}
		BookType bookType=new BookType(Integer.parseInt(id),bookTypeName,bookTypeDesc);
		Connection con = null;
		
		try {
			con = dbUtil.getCon();
			int modifyNum = bookTypeDao.bookTypeModify(con, bookType);
			if (modifyNum == 1) {

				JOptionPane.showMessageDialog(null, "修改成功！");
				this.resetValue();
				this.fillTable(new BookType());

			} else {

				JOptionPane.showMessageDialog(null, "修改失败！");
			}

		} catch (Exception e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
			JOptionPane.showMessageDialog(null, "修改失败！");
		}
	}

	private void jBDeleteActionPerformed(java.awt.event.ActionEvent evt) {
		String id = this.jBookTypeId.getText();
		if ("".equals(id) || id == null) {
			JOptionPane.showMessageDialog(null, "请选择要删除的记录");
			return;
		}
		int n = JOptionPane.showConfirmDialog(null, "确定要删除这条记录吗？");
		if (n == 0) {
			Connection con = null;
			try {
				con = dbUtil.getCon();
				int deleteNum = bookTypeDao.bookTypeDelete(con, id);
				if (deleteNum == 1) {

					JOptionPane.showMessageDialog(null, "删除成功！");
					this.resetValue();
					this.fillTable(new BookType());

				} else {

					JOptionPane.showMessageDialog(null, "删除失败！");
				}

			} catch (Exception e) {
				// TODO Auto-generated catch block
				e.printStackTrace();
				JOptionPane.showMessageDialog(null, "删除失败！");
			}

		}
	}

	private void resetValue() {
		this.jBookTypeId.setText("");
		this.jBookTypeTxt.setText("");
		this.jBookTypeDescTxt.setText("");

	}

	private void jBookTypeTableMousePressed(java.awt.event.MouseEvent evt) {
		int row = jBookTypeTable.getSelectedRow();
		this.jBookTypeId.setText((String) jBookTypeTable.getValueAt(row, 0));
		this.jBookTypeTxt.setText((String) jBookTypeTable.getValueAt(row, 1));
		this.jBookTypeDescTxt.setText((String) jBookTypeTable
				.getValueAt(row, 2));
	}

	private void jBReaserchActionPerformed(java.awt.event.ActionEvent evt) {
		String bookTypeName = this.jBookTypeName.getText();
		BookType bookType = new BookType();
		bookType.setBookTypeName(bookTypeName);
		this.fillTable(bookType);
	}

