	private void jBLoginMouseClicked(java.awt.event.MouseEvent evt) {
		String userName = this.jUserName.getText();
		String password = this.jPassword.getText();
		if ("".equals(userName) || userName == null) {
			JOptionPane.showMessageDialog(null, "用户名不能为空");
			return;
		}

		if ("".equals(password) || password == null) {
			JOptionPane.showMessageDialog(null, "密码不能为空");
			return;
		}

		User user = new User(userName, password);
		Connection con = null;
		try {
			con = dbUtil.getCon();
			User currentUser = userDao.login(con, user);
			if (currentUser != null) {
				//JOptionPane.showMessageDialog(null, "恭喜你，登陆成功！");
				this.dispose();
				new MainFrm().setVisible(true);
			} else {
				JOptionPane.showMessageDialog(null, "对不起，您的用裘或者密ae`！");
			}
		} catch (Exception e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		} finally {
			try {
				dbUtil.colseCon(con);
			} catch (SQLException e) {
				// TODO Auto-generated catch block
				e.printStackTrace();
			}
		}

	}

